     */
    public function store(Request $request)
    {
        $id = Category::find(1);
        $this->validate($request,[
            'name'=>'required',
            'description' => 'required|min:10'
        ]);
        Book::create([
            'name' => $request['name'],
            'description' =>$request['description'],
            'type' => $id->id
        ]);
        return $this->index();
    }

